package com.example.pokemon.entities;

public class Pueblo {
	
	private int id;
	
	private String nombre;
	private String uuid;
	
	
	public int getId() {
        return id;
    }

    public void setId(int id) {
        this.id = id;
    }
	public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }

    public String getUuid() {
        return uuid;
    }

    public void setUuid(String uuid) {
        this.uuid = uuid;
    }
	

}
